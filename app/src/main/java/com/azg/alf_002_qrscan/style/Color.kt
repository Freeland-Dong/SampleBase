fun Color.adjustLevel(level: Int): Color {
    val factor = 1 - (level * 0.1f)
    return Color(
        red = adjustComponent(this.red, factor),
        green = adjustComponent(this.green, factor),
        blue = adjustComponent(this.blue, factor),
        alpha = this.alpha
    )
}
fun adjustComponent(component: Float, factor: Float): Float {
    return min(1f, max(0f, component * factor))
}


fun Color.opacity(alpha: Float): Color {
    return this.copy(
        alpha = alpha
    )
}



fun Color.opacity(alpha: Int): Color {
    val currentAlpha = if(alpha>0) alpha/100f else alpha.toFloat()
    return this.copy(
        alpha = currentAlpha
    )
}






















